                        <div class="ProjectDescription">
<p><strong>When:</strong> 1 - 30 September, 2017.

</p><p><strong>Who:</strong> Jeff, Niki, Lindsay.

</p><p><strong>Where:</strong> Carlos E Restrepo, Medellín, Colombia.

</p><p>Project House is about getting people excited learning by doing whatever it is that interests them. Lindsay contacted us about Anywhere Digs while we were two thirds through with Focus Week Sudamerica. We pitched Project House, and proposed Medellín, Colombia. Colombia boasts sub-$200 RT fare from the US, easy to understand Spanish, and the delightful absense of Escobar-era problems. It’s also really cheap, by gringo standards, at least if your able to avoid the gringo traps. Watch out!

</p><p>Lindsay came to Medellîn interested in expanding her marketing skills, and looking for ways to leverage those skills to earn a living while travelling. We found she had a knack for connecting with other entrepreneurs, local and nomadic. Lindsay started exploring a workshop-based business model for digital nomads. She began demo-ing a brand workshop, leveraging entrepreneurial contacts and digital nomad facebook groups, as well as ads.facebook.com. Both of Lindsay’s brand workshops were full, and had rave client feedback!

</p><p>What other workshops could we run? What could we charge for a workshop? Where should we look for paying clients? Rather than pondering these questions twiddling our thumbs, we decided to just try some other workshops, and explore some schedules, pricing, and advertising channels. But running a workshop requires knowing material. We took a week exploring easy-to-learn video editors before settling on iPhone’s oSnap, and then pilotted an oSnap workshop with our friend Vic, a local contact. Vic runs an organization that organizes team sport opportunities for at-risk youth in suburban Medellín, and used oSnap and our cardboard stage to “snap” a promo video demo-ing his sport.

</p><p>Lindsay decided the video workshop was ready, and ran two more video workshops for her last week at Project House. (More discussion of workshop project execution, deliverables and lessons can be found ((elsewhere)).)

</p><p>Meanwhile, Jeff and Niki were drafting designs on another “digital nomad” product, nicknamed LIA, or language immersion assistant. LIA synthesized usability ideas we’d developed on the ground during Focus Week Sudamerica. By end-of-week, Project House had launched its first app on Android’s App Store, the beta ((ImmersionAssistantPrototype))!... And could start testing it ourselves on the streets, cabs, and cafes of Medellín.

</p><p>To celebrate the conclusion of the first Project House, we spent some tourist time photographing grafiti at Comuna 13 and hiking up a huge hill at Guatapi.

</p><p><strong>Highlights:</strong>
</p><ul><li>Culinary: Carmen in Poblado.
</li><li>Culture: Festival de la cultura y libros.
</li><li>Tourism: Medellín Streetart.
</li></ul>
