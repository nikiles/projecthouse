<p><strong>When:</strong> 2 - 6 October, 2017.

</p><p><strong>Who:</strong> Jeff, Niki, Taylor, Ezra, Jacqueline, Marissa, Johann, Manuela, Daniel, Mariana, Gizelle, and David.

</p><p><strong>Where:</strong> Carlos E Restrepo, Medellín, Colombia.

</p><p>We found Ezra, Jacqueline, and Marissa on workaway.org, where budget travelers can volunteer their time at local projects. We were looking for a few random but energetic contributors to form the core workforce of Project House II, and that was exactly what they were! Additional participants we found through local connections, Manuela and Johann from Venezuela, and Daniel and Mariana from Colombia.

</p><p>When you have only four days to work on something, you have to get started very quickly! We presented the group with 5 project possibilities, four extending prior Project House work and one open-ended technical challenge. The group decided to merge Lindsay’s “workshop business” with the crowd-funding expertise needs of Vic’s local project, they decided to develop a crowdfunding workshop and pilot it with Vic’s colleagues Gizelle and David.

</p><p>For two days we put our heads down to research various aspects of crowd-funding wisdom, then brought it all together into a presentation deck. We reviewed the deck ourselves, then invited over Gizelle and David. We pitched our workshop, and asked for feedback… what was most relevant to them, what was least relevant, what did they most need help on?
</p><p>G &amp D were very enthusiastic about the information and resources we presented them. However, they thought the most valuable workshop to them would be a detailed technical program on one of the pieces of a successful crowdfunding campaign -- wordsmithery, image selection, pitch video, social media advertising.

</p><p>For the final day of Project House II, G &amp nd D came back with their media drive. Gizelle paired up with Ezra and Marissa to the find the best media for their charity-oriented crowdfunding campaign. Jeff and Jacqueline worked with David on a 30 second project pitch, a critical ingredient to successful campaigns. Niki shared visual designs to help G &amp D explain their sport to casual investors. (See final recording here!)

</p><p>We packed up shop the next day to head on our second Colombian excursion: a weekend in Bogotá and cabin camping in Cucuy national park.

</p><p><strong>Highlights:</strong>
</p><ul><li>Culinary: Johann’s home-made empanadas at Project House dinner.
<li>Culture: Colombian nationals in Bogotá, Vic, G & D’s team finish 5th!
<li>Tourism: Cucuy National Park.</ul>

